# 1. Inteiro (int)
idade = 28
print(idade)

# 2. Float
altura = 1.75
print(altura)

# 3. String
nome = "André Ótony"
print(f"Olá, meu nome é {nome}.")

# 4. Boolean
estudando_python = True
print(estudando_python)

# 5. Lista
frutas = ["maçã", "banana", "uva"]
print(frutas[1])  # segunda fruta

# 6. Tupla
cores = ("vermelho", "azul", "verde", "amarelo")
print(cores[0])  # primeira cor

# 7. Dicionário
aluno = {"nome": "João", "idade": 20, "nota": 9.5}
print(aluno["nota"])

# 8. Misturando tipos
dados = [25, 1.80, "Python é legal!", False]
print(dados)
